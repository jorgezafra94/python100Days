# round function

# round regular behavior
print((8 / 3), round(8 / 3))
print((7 / 2), round(7 / 2))
print((4 / 3), round(4 / 3))
print()
# round with round on decimals instead of int
print("round in decimals not in int")
print((8 / 3), round(8 / 3, 2))
print((7 / 2), round(7 / 2, 2))
print((4 / 3), round(4 / 3, 2))
